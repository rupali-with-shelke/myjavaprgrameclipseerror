package javaprogramconcept1;

public class PractisePattern {

	public static void main(String[] args) {

		for(int i=1;i<=4;i++) {
			
			for(int j=3;j>=i;j--) {
				
				System.out.print(" ");//for space we write j loop 
			}
			for(int k=1;k<=i;k++) {//for printing stsr we write k loop
				
				System.out.print("*");
			}
			System.out.println();
			
		}
		
		
		
		
		
		
		
   	}

}
