package javaprogramconcept1;

public class SwapingNumberWithThirdVariable {

	public static void main(String[] args) {
		
	int temp;
	int a=4;
	int b=5;
	System.out.println("Before swaping   :a=" +a+" and b= "+b);
	
	temp=a;
	a=b;
	b=temp;
	System.out.println("After swaping   :a=" +a+" and b= "+b);
	
		

	}

}
